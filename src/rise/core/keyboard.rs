pub fn update() {
    unsafe {
        KEYBOARD_JUST_PRESSED = [false; 159];
        KEYBOARD_JUST_RELEASED = [false; 159];
    }
}

pub fn process_input(event: &winit::event::KeyboardInput) {
    //unimplemented!();

    match &event.virtual_keycode {
        &Some(keycode) => {
            let key = keycode as usize;

            match &event.state {
                winit::event::ElementState::Pressed => {
                    unsafe {
                        KEYBOARD_PRESSED[key] = true;
                        KEYBOARD_JUST_PRESSED[key] = true;
                    }
                },
                winit::event::ElementState::Released => {
                    unsafe {
                        KEYBOARD_PRESSED[key] = false;
                        KEYBOARD_JUST_RELEASED[key] = true;
                    }   
                }
            }

        },
        None => {}
    }
}

pub fn is_pressed(keycode: Key) -> bool {
    unsafe {
        KEYBOARD_PRESSED[keycode as usize]
    }
}